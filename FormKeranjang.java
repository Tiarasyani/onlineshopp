    private void tampilkanIsiKeranjang() {
        StringBuilder isi = new StringBuilder();

        int i = 0;
        for (ShoppingCart.CartItem item : cart.getItems()) {
            Product p = item.getProduct();

            isi.append("[").append(i).append("] ")
                .append(p.getName())
                .append(" x ")
                .append(item.getQuantity())
                .append(" = ")
                .append(Product.formatRupiah(p.calculateFinalPrice() * item.getQuantity()))
                .append("\n   Detail: ")
                .append(p instanceof Detailable ? ((Detailable)p).getDetail() : "Tidak ada")
                .append("\n\n");
            i++;
        }

        textAreaCart.setText(isi.toString());
        textAreaCart.setEditable(false);
        lblTotalHarga.setText("Rp " + cart.getTotalPrice());

    }
    
    private int hitungTotal() {
        int total = 0;
        for (ShoppingCart.CartItem item : cart.getItems()) {
            Product p = item.getProduct();
            total += p.calculateFinalPrice() * item.getQuantity();
        }
        return total;
    }

    private void hapusBarangTerpilih() {
        int caretPos = textAreaCart.getCaretPosition();
        String[] lines = textAreaCart.getText().split("\n");

        int index = -1;
        for (String line : lines) {
            if (line.contains("[")) {
                index++;
            }
            if (caretPos <= textAreaCart.getText().indexOf(line) + line.length()) {
                break;
            }
        }

        if (index < 0 || index >= cart.getItems().size()) {
            JOptionPane.showMessageDialog(this, "Pilih produk yang valid.");
            return;
        }

        int konfirmasi = JOptionPane.showConfirmDialog(
            this,
            "Yakin ingin menghapus produk dari keranjang?",
            "Konfirmasi",
            JOptionPane.YES_NO_OPTION
        );

        if (konfirmasi == JOptionPane.YES_OPTION) {
            cart.getItems().remove(index);
            tampilkanIsiKeranjang();
        }
    }

