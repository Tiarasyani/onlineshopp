    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int caretPos = textAreaCart.getCaretPosition(); // posisi kursor
        int currentLine = -1;

        try {
            currentLine = textAreaCart.getLineOfOffset(caretPos);
        } catch (BadLocationException e) {
            JOptionPane.showMessageDialog(this, "Gagal membaca posisi kursor.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int barisTerpilih = -1;

        try {
            String[] lines = textAreaCart.getText().split("\n");
            for (int i = currentLine; i >= 0; i--) {
                String line = lines[i].trim();
                if (line.startsWith("[") && line.contains("]")) {
                    String nomorStr = line.substring(1, line.indexOf("]"));
                    barisTerpilih = Integer.parseInt(nomorStr);
                    break;
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal mendeteksi produk yang dipilih.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (barisTerpilih < 0 || barisTerpilih >= cart.getItems().size()) {
            JOptionPane.showMessageDialog(this, "Pilih baris produk yang valid untuk dihapus.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int konfirmasi = JOptionPane.showConfirmDialog(
            this,
            "Yakin ingin menghapus produk dari keranjang?",
            "Konfirmasi",
            JOptionPane.YES_NO_OPTION
        );

        if (konfirmasi == JOptionPane.YES_OPTION) {
            cart.getItems().remove(barisTerpilih);
            tampilkanIsiKeranjang();
        }

    }//GEN-LAST:event_btnHapusActionPerformed

