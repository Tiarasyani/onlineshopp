    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
        // TODO add your handling code here:
        String nama = txtNama.getText().trim();
        String alamat = txtAlamat.getText().trim();
        String bayarStr = txtBayar.getText().trim();

        if (nama.isEmpty() || alamat.isEmpty() || bayarStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Isi semua data!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
           double bayar = 0;
           double kembalian = 0;
        try {
            bayar = Double.parseDouble(bayarStr);
            if (bayar < totalHarga) {
                JOptionPane.showMessageDialog(this, "Uang tidak cukup!", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
            }

            kembalian = bayar - totalHarga;
            txtKembalian.setText(String.valueOf(kembalian));

            JOptionPane.showMessageDialog(this, "Pembayaran berhasil!\nTerima kasih, " + nama, "Sukses", JOptionPane.INFORMATION_MESSAGE);

            new FormBeranda(cart).setVisible(true);
            this.dispose();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Masukkan nominal uang yang valid!", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        Order order = new Order(nama, alamat, totalHarga, bayar, kembalian, new ArrayList<>(cart.getItems()));
        cart.addOrder(order);
        cart.clear();  // Kosongkan keranjang setelah bayar


    }//GEN-LAST:event_btnBayarActionPerformed

