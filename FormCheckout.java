    public FormCheckout(ShoppingCart cart, int totalHarga) {
        initComponents();
        this.cart = cart;
        this.totalHarga = totalHarga;
        


        double total = 0;
        for (ShoppingCart.CartItem item : cart.getItems()) {
            total += item.getProduct().calculateFinalPrice() * item.getQuantity();
        }



        txtTotalHarga.setText(String.valueOf(totalHarga));
        txtTotalHarga.setEditable(false);
        txtKembalian.setEditable(false);
        
        txtBayar.addKeyListener(new java.awt.event.KeyAdapter() {
            public void keyReleased(java.awt.event.KeyEvent evt) {
                hitungKembalian();
            }
        });

    }
    
    private void hitungKembalian() {
        try {
            int bayar = Integer.parseInt(txtBayar.getText());
            int kembalian = bayar - totalHarga;

            if (kembalian < 0) {
                txtKembalian.setText("Pembayaran kurang");
            } else {
                txtKembalian.setText("Rp " + kembalian);
            }
        } catch (NumberFormatException e) {
            txtKembalian.setText("Masukkan angka");
        }
    }
    
        

    

